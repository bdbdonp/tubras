-----------------------------------------------------------------------------
--                              o n Q u i t
-----------------------------------------------------------------------------
local function onQuit(event)
    app:stopRunning()
    return 1
end

-----------------------------------------------------------------------------
--                          i n i t i a l i z e
-----------------------------------------------------------------------------
local function initialize()
end

-----------------------------------------------------------------------------
--                              e n t e r
-----------------------------------------------------------------------------
local function enter() 
    app:setBGColor(0, 0, 0)
    app:acceptEvent('quit', onQuit)
end

-----------------------------------------------------------------------------
--                               e x i t
-----------------------------------------------------------------------------
local function exit()
    app:removeEvent('quit', onQuit)
end

-----------------------------------------------------------------------------
--                              p a u s e
-----------------------------------------------------------------------------
local function pause()
end

-----------------------------------------------------------------------------
--                             r e s u m e
-----------------------------------------------------------------------------
local function resume(stateInfo)
end

-----------------------------------------------------------------------------
--                          M e n u S t a t e
-----------------------------------------------------------------------------
MenuState = {
    name='Menu',
    initialize = initialize,
    enter = enter,
    exit = exit,
    pause = pause,
    resume = resume,
}

